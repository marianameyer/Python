# Faça um programa que leia um número inteiro qualquer e mostre na tela sua tabuada.

# Coleta de dados do usuário
num = int(input('Digite um número para saber sua tabuada: '))

# Print
print('-'*14)
print(f'{num} x  1 = {num*1:3}. \n{num} x  2 = {num*2:3}. \n{num} x  3 = {num*3:3}.')
print(f'{num} x  4 = {num*4:3}. \n{num} x  5 = {num*5:3}. \n{num} x  6 = {num*6:3}.')
print(f'{num} x  7 = {num*7:3}. \n{num} x  8 = {num*8:3}. \n{num} x  9 = {num*9:3}.')
print(f'{num} x 10 = {num*10:3}.')
